void BgStage1::Draw()
{
	// 画面全体を空色で塗り潰す
	DrawBox(0, 0, Game::kScreenWidth, Game::kScreenHeight, 0xa0d8ef, true);

	// マップチップの描画
	for (int y = 0; y < kChipNumY; y++)
	{
		for (int x = 0; x < kChipNumX; x++)
		{			
			int posX = kChipWidth * x * kScale + kColChipAdjustmentX + m_pCamera->m_drawOffset.x;
			int posY = kChipHeight * y * kScale - kAllChipHeight;

			// 画面外は描画しない
			if (posX < 0 - kChipWidth) continue;
			if (posX > Stage1::kBgStage1Width) continue;
			if (posY < 0 - kChipWidth) continue;
			if (posY > Game::kScreenHeight) continue;

			// 地面ブロック
			if (kChipSetData[y][x] == 1)
			{
				DrawRotaGraph(posX , posY, kScale, 0, m_handle001, false);
			}
			// ？ブロック
			if (kChipSetData[y][x] == 2)
			{
				DrawRotaGraph(posX, posY, kScale, 0, m_handle002, false);
			}
			// 土管ブロック
			if (kChipSetData[y][x] == 3)
			{
				DrawRotaGraph(posX, posY, kScale, 0, m_handle003, false);
			}
			// レンガブロック
			if (kChipSetData[y][x] == 4)
			{
				DrawRotaGraph(posX, posY, kScale, 0, m_handle004, false);
			}
			// 階段ブロック
			if (kChipSetData[y][x] == 5)
			{
				DrawRotaGraph(posX, posY, kScale, 0, m_handle005, false);
			}
		}
	}
}

/// <summary>
/// 指定したマップチップの矩形と当たっているか判定
/// </summary>
/// <param name="rect">プレイヤーの当たり判定</param>
/// <param name="ChipRect">チップの当たり判定</param>
/// <returns></returns>
bool BgStage1::IsCollision(Rect rect, Rect& ChipRect)
{
	for (int y = 0; y < kChipNumY; y++)
	{
		for (int x = 0; x < kChipNumX; x++)
		{
			// 壁以外とは当たらない
			if (kChipSetData[y][x] == 0) continue;

			int chipLeft = static_cast<int>(x * kChipWidth * kScale - kColChipAdjustmentLeft);
			int chipRight = static_cast<int>(chipLeft + kChipWidth + kColChipAdjustmentX);
			int chipTop = static_cast<int>(y * kChipHeight * kScale - kColChipAdjustmentY);
			int chipBottom = static_cast<int>(chipTop + kChipHeight * kScale);

			// 絶対に当たらないパターンをはじく
			if (chipLeft > rect.m_right) continue;
			if (chipTop > rect.m_bottom) continue;
			if (chipRight < rect.m_left) continue;
			if (chipBottom < rect.m_top) continue;

			// ぶつかったマップチップの矩形を設定する
			ChipRect.m_left = static_cast<int>(chipLeft);
			ChipRect.m_right = static_cast<int>(chipRight);
			ChipRect.m_top = static_cast<int>(chipTop);
			ChipRect.m_bottom = static_cast<int>(chipBottom);

			// いずれかのマップチップと当たっていいたら終了
			return true;
		}
	}
	// 全てのマップチップをチェックして1つも当たっていなければ当たっていない
	return false;
}
