void BgStage::Draw()
{
	// 画面全体を空色で塗り潰す
	DrawBox(0, 0, Game::kScreenWidth, Game::kScreenHeight, 0xa0d8ef, true);

	// 雲の描画
	for (const auto& cloud : kClouds) 
	{
		DrawRotaGraph(static_cast<int>(cloud.x + m_pCamera->m_drawOffset.x), static_cast<int>(cloud.y), 0.7f, 0, (cloud.handle == 6 ? m_handle006 : m_handle007), true);
	}
	// 草の描画
	for (const auto& bushX : kBushes) 
	{
		DrawRotaGraph(static_cast<int>(bushX + m_pCamera->m_drawOffset.x), kBushPosY, 0.3f, 0, m_handle008, true);
	}

	// マップチップの描画
	for (int y = 0; y < kChipNumY; y++)
	{
		for (int x = 0; x < kChipNumX; x++)
		{			
			int posX = static_cast<int>(kChipWidth * x * kScale + m_pCamera->m_drawOffset.x);
			int posY = static_cast<int>(kChipHeight * y * kScale - kAllChipHeight);

			// 画面外は描画しない
			if (posX < 0 - kChipWidth) continue;
			if (posX > Stage1::kBgStageWidth) continue;
			if (posY < 0 - kChipWidth) continue;
			if (posY > Game::kScreenHeight) continue;

			// 描画するハンドルを選択
			int handle = -1;
			switch (kChipSetData[y][x])
			{
			case 1: handle = m_handle001; break; // 地面ブロック
			case 2: handle = m_handle002; break; // ？ブロック
			case 3: handle = m_handle003; break; // 土管ブロック
			case 4: handle = m_handle004; break; // レンガブロック
			case 5: handle = m_handle005; break; // 階段ブロック
			}

			// ハンドルが有効なら描画
			if (handle != -1)
			{
				DrawRotaGraph(posX, posY, kScale, 0, handle, false);
			}
		}
	}
}

/// <summary>
/// 指定したマップチップの矩形と当たっているか判定
/// </summary>
/// <param name="rect">プレイヤーの当たり判定</param>
/// <param name="ChipRect">チップの当たり判定</param>
/// <returns></returns>
bool BgStage::IsCollision(Rect rect, Rect& ChipRect)
{
	for (int y = 0; y < kChipNumY; y++)
	{
		for (int x = 0; x < kChipNumX; x++)
		{
			// 壁以外とは当たらない
			if (kChipSetData[y][x] == 0) continue;

			// マップチップの矩形を設定
			int chipLeft = static_cast<int>(x * kChipWidth * kScale - kColChipAdjustmentLeft);
			int chipRight = static_cast<int>(chipLeft + kChipWidth + kColChipAdjustmentX);
			int chipTop = static_cast<int>(y * kChipHeight * kScale - kColChipAdjustmentY);
			int chipBottom = static_cast<int>(chipTop + kChipHeight * kScale);

			// 絶対に当たらないパターンをはじく
			if (chipLeft > rect.m_right) continue;
			if (chipTop > rect.m_bottom) continue;
			if (chipRight < rect.m_left) continue;
			if (chipBottom < rect.m_top) continue;

			// ぶつかったマップチップの矩形を設定する
			ChipRect.m_left = static_cast<float>(chipLeft);
			ChipRect.m_right = static_cast<float>(chipRight);
			ChipRect.m_top = static_cast<float>(chipTop);
			ChipRect.m_bottom = static_cast<float>(chipBottom);

			// いずれかのマップチップと当たっていいたら終了
			return true;
		}
	}
	// 全てのマップチップをチェックして1つも当たっていなければ当たっていない
	return false;
}

